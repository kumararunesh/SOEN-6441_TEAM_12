package org.GamePlay;

/**
 * This is the Abstract class which contains the declaration of Card Order Function.
 */
public abstract class CardsAbstract {

    /**
     * The function implements the functionality of Bomb card
     */
    public void Bomb(){};
    /**
     * The function implements the functionality of Blockade card
     */
    public void Blockade(){};
    /**
     * The function implements the functionality of Airlift card
     */
    public void Airlift(){};
    /**
     * The function implements the functionality of Diplomacy card
     */
    public void Diplomacy(){};

}
